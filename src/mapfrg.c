void draw_map_fragment(BITMAP *map_to_draw, int clear_only){

		int x,y;
		int xs,ys,yc;
		BITMAP *tile_to_draw;
		short current_tile;

	    if (clear_only) {
		    rectfill(map_to_draw,0,0,map_to_draw->w,map_to_draw->h,colors_to24bits(MAP_COLOR));
	    	return;
	    }

		//blank off the edges
	    //top row
	    rectfill(map_to_draw,0,0,map_to_draw->w,TILE_HEIGHT/2,colors_to24bits(MAP_COLOR));
	    //bottom row
	    rectfill(map_to_draw,0,map_to_draw->h-TILE_HEIGHT/2,map_to_draw->w,map_to_draw->h,colors_to24bits(MAP_COLOR));
	    //left column
	    rectfill(map_to_draw,0,0,TILE_WIDTH/2,map_to_draw->h,colors_to24bits(MAP_COLOR));
	    //right column
	    rectfill(map_to_draw,map_to_draw->w-TILE_WIDTH/2,0,map_to_draw->w,map_to_draw->h,colors_to24bits(MAP_COLOR));


	    for(x=0;x<FRG_X_SIZE;x++)
		    for(y=0;y<FRG_Y_SIZE;y++){
		        xs=x*TILE_WIDTH;
		        ys=y*TILE_HEIGHT;
		        if (x%2) ys+=TILE_HEIGHT/2;
		        yc=y- map_frg_big_table_new[map_frg_big_table_idx].map_frg_big_table_offset*(x+1)%2;
		        current_tile= map_frg_big_table_new[map_frg_big_table_idx].map_frg_big_table[Max(yc,0)][x];
		        if (current_tile==-1 || yc<0)
		        	masked_blit(black_hex_bmp,map_to_draw,0,0,xs,ys,TILE_FULL_WIDTH,TILE_HEIGHT);
		        else{
					switch(showWeather){
						case 0:tile_to_draw = til_bmp[current_tile]; break;
						case 1:tile_to_draw = til_bmp_mud[current_tile];break;
						case 2:tile_to_draw = til_bmp_snow[current_tile];break;
						default: tile_to_draw = NULL; break;
					}
		        	masked_blit(tile_to_draw,map_to_draw,0,0,xs,ys,TILE_FULL_WIDTH,TILE_HEIGHT);
		        }
		    }
}

int map_comp_f(const void *ii, const void *jj)
{
    struct map_fragment *i=(struct map_fragment *)ii;
    struct map_fragment *j=(struct map_fragment *)jj;

     return i->dx*i->dy-j->dx*j->dy;
}

void sort_map_fragments(){
	int i,x,y,dx,dy;

	//find size of fragments
	for(i=0;i<map_frg_big_table_size;i++){
		//find max dy, dx
		dx=0;
		dy=0;
	    for(x=0;x<FRG_X_SIZE;x++)
		    for(y=0;y<FRG_Y_SIZE;y++){
		    	if (map_frg_big_table_new[i].map_frg_big_table[y][x] != -1 && y>dy) dy=y;
		    	if (map_frg_big_table_new[i].map_frg_big_table[y][x] != -1 && x>dx) dx=x;
		    }
	    map_frg_big_table_new[i].dx=dx+1;
	    map_frg_big_table_new[i].dy=dy+1;
	}
	qsort(map_frg_big_table_new,map_frg_big_table_size,sizeof(struct map_fragment),map_comp_f );
}
